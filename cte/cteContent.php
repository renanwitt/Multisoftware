            <p><b>IMPORTANTE: </b>Para emitir um CTE do tipo subcontratação, você precisará do arquivo XML de um CTE salvo em seu dispositivo.</p>
          
            <p>Estando acessado no sistema MultiCTE, acesse o menu emissões e clique em CTE. Nesta página, selecione a opção “importar CTE”. Uma janela será aberta em sua tela, sendo que no canto inferior esquerdo uma opção denominada “importar arquivo” espera pelo seu clique. Ao clicar em “importar arquivo”, localize onde salvou o arquivo XML do CTE em seu dispositivo e em seguida selecione-o, por fim, importe-o clicando em “enviar arquivo”.</p>

            <p>Após importar o CTE para o sistema, automaticamente a tela de emissões surgirá na sua tela. Assim como na emissão de um CTE normal, navegue por aba e preencha as informações necessárias com os valores que desejar. Um diferencial muito comum em uma subcontratação é a escolha do tomador do serviço. Na maioria das vezes, o tomador é o emitente do CTE que foi importado. Para inserir o emitente do CTE que fora importado como tomador da subcontratação, vá até a aba “outros” e selecione “tomador”, em seguida informe o CNPJ do tomador no campo correspondente.</p>

            <p>Outro diferencial de uma subcontratação, é a informação contida no campo de observações. Fica a critério inserir uma frase mencionando que tal documento é um frete subcontratado de outra empresa. A seguir, segue uma frase como exemplo:</p>

            <p>FRETE SUBCONTRATADO DA EMPRESA ********** DE CNPJ ************* REFERENTE AO CTE ***** CHAVE DE ACESSO ************************************</p>

            <p>Após preencher todos os campos em todas as abas, certifique que as informações estão de acordo e finalize a emissão clicando em “emitir CT-e”. Pronto, documento emitido.</p>
