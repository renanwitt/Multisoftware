            <p>Estando acessado no sistema MultiCTE, acesse o menu emissões e clique em CTE. Nesta página, use os filtros para localizar o CTE que deseja usar como base para emitir um complemento. Após localizar o CTE, clique no menu “opções” disposto a direita da tela e selecione “CT-e complementar”.</p>

            <p>Após selecionar “CT-e complementar”, a tela de emissões será aberta em sua tela. Como citado anteriormente, apenas campos de valores de frete e observações podem ser alterados. Na aba “serviços e impostos”, informe o valor do complementar, bem como a tributação (a tributação padrão será a mesma do CTE original) e por fim, fica por critério ou não em acrescentar uma frase de observação no campo de observações. A seguir, uma frase como exemplo:</p>

            <p><i><u>COMPLEMENTAR REFERENTE AO CTE 000</u></i></p>

          <p>Por fim, revise os campos preenchidos e clique em “Emitir CTE”.</p>
